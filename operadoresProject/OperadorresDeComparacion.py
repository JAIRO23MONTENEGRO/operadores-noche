#Ejemplo con operadores de Comparacion

m = int(input("Escriba el valor de m: "))
n = int(input("Escriba el valor de n: "))

print(m == n )  #Igualdad
print(m > n)    #Mayor que >
print(m < n)    #Menor que <
print(m >= n)   #Mayor o igual que >=
print(m <= n)   #Menor o igual que <=
print(m != n)   #No igual o diferente !
